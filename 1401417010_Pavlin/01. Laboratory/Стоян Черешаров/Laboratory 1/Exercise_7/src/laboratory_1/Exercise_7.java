package laboratory_1;

public class Exercise_7 {

    public static void main(String[] args) {

        int a, b;
        a = 5;
        b = 6;
        
        System.out.println(a + "==" + b + " : " + (a == b));
        System.out.println(a + "<" + b + " : " + (a < b ));
        System.out.println(a + ">" + b + " : " + (a > b ));
        System.out.println(a + "<=" + b + " : " + (a <= b ));
        System.out.println(a + ">=" + b + " : " + (a >= b ));
        System.out.println(a + "!=" + b + " : " + (a != b )); 
    } 
}
