package laboratory_1;

public class Exercise_4 {

    public static void main(String[] args) {

        int a, b;
        a = 5;
        b = 10;
        
        a = a++;
        System.out.println(a);
        a = ++a;
        System.out.println(a);
        
        System.out.println("-------------");
        
        b = b--;
        System.out.println(b);
        b = --b;
        System.out.println(b);
    }  
}
